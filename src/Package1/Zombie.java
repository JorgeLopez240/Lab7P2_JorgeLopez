
package Package1;

public class Zombie {
    
    private String nombre;
    private double ataque;
    private double vida;

    public Zombie() {
    }

    public Zombie(String nombre, double ataque, double vida) {
        this.nombre = nombre;
        this.ataque = ataque;
        this.vida = vida;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public double getAtaque() {
        return ataque;
    }

    public void setAtaque(double ataque) {
        this.ataque = ataque;
    }

    public double getVida() {
        return vida;
    }

    public void setVida(double vida) {
        this.vida = vida;
    }

    @Override
    public String toString() {
        return nombre;
    }
    
    
    
}
